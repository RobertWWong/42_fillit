int			ft_sqrt(int	num)
{
	float	x;
	float	y;
	float	e;

	x = num;
	y = 1;
	e = 0.000001;
	while (x - y > e)
	{
		x = (x + y) / 2;
		y = num / x;
	}
	return ((int)x);
}
